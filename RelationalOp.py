#Relational operator with int data type
a=20
b=10
print("a>b=",a>b)
print("a>=b=",a>=b)
print("a<b=",a<b)
print("a<=b=",a<=b)

#Relational operator with str data type
a='Rishabh'
b='Jaiswal'
print("a>b=",a>b)
print("a>=b=",a>=b)
print("a<b=",a<b)
print("a<=b=",a<=b)

#Relational operator with bool data type
