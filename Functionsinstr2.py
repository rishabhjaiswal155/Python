#Functions like upper(),lower(),swapcase(),title(),capitalize(),startswith(),endswith()

s="Learning python is very easy,because of its concise code."
print("upper():",s.upper())
print("lower():",s.lower())
print("swapcase():",s.swapcase())
print("title():",s.title())
print("capitalize():",s.capitalize())
print("startswith():",s.startswith("Learning"))
print("endswith():",s.endswith("Code"))